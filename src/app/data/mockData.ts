function generatePriceHistory(basePrice: number) {
  const history = [];
  const now = new Date();
  for (let i = 29; i >= 0; i--) {
    const d = new Date(now);
    d.setDate(d.getDate() - i);
    const variance = (Math.random() - 0.5) * basePrice * 0.08;
    history.push({
      date: d.toLocaleDateString("en-PK", { month: "short", day: "numeric" }),
      price: Math.round((basePrice + variance) / 100) * 100,
    });
  }
  return history;
}

function makeStores(base: number, names: string[]): PhoneStore[] {
  return names.map((s, i) => ({
    store: s,
    price: base + i * Math.round(base * 0.03),
    delivery: ["1-2 days", "2-3 days", "3-5 days", "Same day"][i % 4],
    rating: parseFloat((4.5 - i * 0.2).toFixed(1)),
    inStock: i < 3,
    logo: s[0],
  }));
}

